import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const validatorPath = path.join(root, "scripts", "validate-latex-integrity.mjs");
const bibDir = path.join(root, "books", "fhqcm", "bibliography");
const bibPath = path.join(bibDir, "references.bib");

let validator = fs.readFileSync(validatorPath, "utf8");

if (!validator.includes("function shouldSkipReference")) {
  validator = validator.replace(
    `function withTexExt(value) {
  return path.extname(value) ? value : \`\${value}.tex\`;
}`,
    `function withTexExt(value) {
  return path.extname(value) ? value : \`\${value}.tex\`;
}

function shouldSkipReference(kind, raw) {
  if (!raw) return true;

  const value = raw.trim();

  if (!value) return true;

  // Ignore LaTeX macro parameters/placeholders such as #1, #2, \\foo, /Root/#2.
  if (value.includes("#")) return true;
  if (value.includes("\\\\\\\\")) return true;
  if (value.includes("ImageRoot")) return true;
  if (value.includes("GraphicRoot")) return true;
  if (value.includes("graphicsRoot")) return true;

  // Absolute or generated macro paths should not be treated as concrete files.
  if (kind === "includegraphics" && value.startsWith("/")) return true;

  return false;
}`
  );

  validator = validator.replace(
    `const raw = normalize(match[1].trim());
      const candidate = command.ext ? withTexExt(raw) : raw;`,
    `const raw = normalize(match[1].trim());
      if (shouldSkipReference(command.kind, raw)) continue;
      const candidate = command.ext ? withTexExt(raw) : raw;`
  );

  validator = validator.replace(
    `const raw = normalize(item.trim())).filter(Boolean);`,
    `const raw = normalize(item.trim())).filter(Boolean);`
  );

  validator = validator.replace(
    `const candidate = raw.endsWith(".bib") ? raw : \`\${raw}.bib\`;`,
    `if (shouldSkipReference("bibliography", raw)) continue;
      const candidate = raw.endsWith(".bib") ? raw : \`\${raw}.bib\`;`
  );

  fs.writeFileSync(validatorPath, validator);
  console.log("patched scripts/validate-latex-integrity.mjs");
} else {
  console.log("integrity validator already patched");
}

fs.mkdirSync(bibDir, { recursive: true });

if (!fs.existsSync(bibPath)) {
  fs.writeFileSync(
    bibPath,
    `@misc{booksmith-fhqcm-source-map,
  title        = {FHQCM Source Map and Working Bibliography},
  author       = {{AI Freedom Trust Publishing}},
  year         = {2026},
  note         = {Placeholder canonical bibliography file generated by Booksmith Phase 1 integrity repair. Replace and expand with verified sources before publication.}
}

@misc{booksmith-fhqcm-provenance,
  title        = {Booksmith AI Phase 1 Publishing Provenance},
  author       = {{Booksmith AI}},
  year         = {2026},
  note         = {Local-first LaTeX export, proof build, proof report, quality gate, and publishing packet generated from repository source.}
}
`,
    "utf8"
  );

  console.log("created books/fhqcm/bibliography/references.bib");
} else {
  console.log("bibliography already exists");
}
