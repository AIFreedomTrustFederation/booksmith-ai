import fs from "node:fs/promises";
import path from "node:path";

import type { RepairRule } from "./types";

export const missingBibliographyRule: RepairRule = {
  id: "missing-bibliography",
  label: "Create missing canonical bibliography file",

  supports(diagnostic) {
    return diagnostic.type === "missing-bibliography";
  },

  canAutoFix() {
    return true;
  },

  async apply(context, diagnostic) {
    const bibDir = path.join(context.exportDir, "bibliography");
    const bibPath = path.join(bibDir, "references.bib");

    await fs.mkdir(bibDir, { recursive: true });

    try {
      await fs.access(bibPath);

      return {
        ruleId: this.id,
        diagnosticId: diagnostic.id,
        fixed: false,
        changedFiles: [],
        message: "Bibliography already exists.",
      };
    } catch {
      await fs.writeFile(
        bibPath,
        `@misc{booksmith-${context.slug}-working-bibliography,
  title        = {Booksmith Working Bibliography for ${context.slug}},
  author       = {{AI Freedom Trust Publishing}},
  year         = {2026},
  note         = {Generated by Booksmith Proof Repair v2. Replace with verified publication references before final release.}
}
`,
        "utf8",
      );

      return {
        ruleId: this.id,
        diagnosticId: diagnostic.id,
        fixed: true,
        changedFiles: [path.relative(context.root, bibPath)],
        message: "Created export-local bibliography/references.bib.",
      };
    }
  },
};
